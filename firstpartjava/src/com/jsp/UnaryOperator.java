package com.jsp;

public class UnaryOperator {
	public static void main(String[] args) {
		int x=5;
		int y=x++;
		System.out.println(y);
		System.out.println(x);
		
		double y1=20.0;
		double x1=y1++;
		System.out.println(x1);
		System.out.println(y1);
		
		
		int x2=10;
		int y2=--x2;
		System.out.println(y2);
		System.out.println(x2);
		
		
	}

}
